/*
Toán tử số học
    +       -> Cộng
    -       -> Trừ
    *       -> Nhân
    **      -> Lũy thừa
    /       -> Chia
    %       -> Chia lấy dư
    ++      -> Tăng 1 giá trị số
    --      -> Giảm 1 giá trị số
*/

// 4 toán tử cơ bản (+ - * /)

var a = 7;
var b = 3;
console.log('a = ' + a);
console.log('b = ' + b);
var c = a + b;
console.log('a cộng b = ' + c);

// Lũy thừa (**)

var d = a ** b;
console.log('a lũy thừa b = ' + d);

// Chia lấy dư (%)

var e = a % b;
console.log('a chia lấy dư cho b = ' + e);

// Tăng 1 giá trị số

var c = 10;
c++;
console.log('Tăng 1 giá trị của c = ' + c);

// Giảm 1 giá trị số 

var d = 10;
d--;
console.log('Giảm 1 giá trị của d = ' + d);